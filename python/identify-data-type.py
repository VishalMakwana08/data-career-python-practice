#Identify the Data Type

#int
value=10
print(f"Value:{value} Type:{type(value)}")

#float
value=10.10
print(f"Value:{value} Type:{type(value)}")

#complex
value=10+10j
print(f"Value:{value} Type:{type(value)}")

#bool
value=True
print(f"Value:{value} Type:{type(value)}")

#str
value="Hello"
print(f"Value:{value} Type:{type(value)}")